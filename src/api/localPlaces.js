// Функция для извлечения улицы и дома
const parseAddress = (address) => {
  const lowerAddr = address.toLowerCase()
  
  // Ищем улицу в базе
  for (const [key, value] of Object.entries(localPlaces)) {
    if (lowerAddr.includes(key)) {
      return value
    }
  }
  
  return null
}

// Функция для поиска в локальной базе
export const findLocalPlace = (address) => {
  if (!address) return null
  
  const lowerAddress = address.toLowerCase().trim()
  
  // Точное совпадение
  if (localPlaces[lowerAddress]) {
    return localPlaces[lowerAddress]
  }
  
  // Поиск по улице (без номера дома)
  const parsed = parseAddress(address)
  if (parsed) {
    return parsed
  }
  
  // Поиск по частичному совпадению
  for (const [key, value] of Object.entries(localPlaces)) {
    if (lowerAddress.includes(key)) {
      return value
    }
  }
  
  return null
}